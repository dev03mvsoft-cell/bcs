<script>
// Language search functionality
document.addEventListener('DOMContentLoaded', function() {
    const searchInput = document.getElementById('languageSearchInput');
    const languageOptions = document.querySelectorAll('.language-option');
    const categories = document.querySelectorAll('.language-category');
    const noResults = document.getElementById('languageNoResults');
    
    if (searchInput) {
        searchInput.addEventListener('input', function() {
            const searchTerm = this.value.toLowerCase().trim();
            let visibleCount = 0;
            
            // Search across all options
            languageOptions.forEach(option => {
                const langName = option.querySelector('.lang-name').textContent.toLowerCase();
                const langCode = option.querySelector('.lang-code').textContent.toLowerCase();
                
                if (langName.includes(searchTerm) || langCode.includes(searchTerm) || searchTerm === '') {
                    option.style.display = 'flex';
                    visibleCount++;
                } else {
                    option.style.display = 'none';
                }
            });
            
            // Hide/Show category headers based on visibility of their children
            categories.forEach(cat => {
                const grid = cat.nextElementSibling;
                if (grid && grid.classList.contains('language-grid')) {
                    const hasVisibleItems = Array.from(grid.children).some(child => child.style.display !== 'none');
                    cat.style.display = hasVisibleItems ? 'flex' : 'none';
                }
            });
            
            // Show/hide no results message
            if (visibleCount === 0 && searchTerm !== '') {
                noResults.style.display = 'flex';
            } else {
                noResults.style.display = 'none';
            }
        });
    }
    
    // Clear search when modal is closed
    const modal = document.getElementById('languageModal');
    if (modal) {
        modal.addEventListener('hidden.bs.modal', function() {
            if (searchInput) {
                searchInput.value = '';
                searchInput.dispatchEvent(new Event('input'));
            }
        });
    }
});
</script>
