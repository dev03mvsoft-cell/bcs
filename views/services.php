<script>
    document.addEventListener('DOMContentLoaded', function() {
        const cards = document.querySelectorAll('.service-card-v2');
        const panel = document.getElementById('serviceDetailPanel');
        const backdrop = document.getElementById('serviceBackdrop');
        const closeBtn = document.getElementById('closeDetail');

        // Panel Content Elements
        const panelTitle = document.getElementById('panelTitle');
        const panelTag = document.getElementById('panelTag');
        const panelIntro = document.getElementById('panelIntro');
        const panelDesc = document.getElementById('panelDesc');
        const panelImage = document.getElementById('panelImage');
        const panelFeatures = document.getElementById('panelFeatures');
        const panelFeaturesTitle = document.getElementById('panelFeaturesTitle');

        function openPanel(card) {
            const title = card.getAttribute('data-service-title');
            const tag = card.getAttribute('data-service-tag');
            const intro = card.getAttribute('data-service-intro');
            const desc = card.getAttribute('data-service-desc');
            const image = card.getAttribute('data-service-image');
            const featuresTitle = card.getAttribute('data-service-features-title') || 'Key Capabilities';
            const features = card.getAttribute('data-service-features').split(',');

            panelTitle.textContent = title;
            panelTag.textContent = tag;
            panelIntro.textContent = intro;
            panelDesc.textContent = desc;
            panelImage.src = image;
            panelFeaturesTitle.textContent = featuresTitle;

            // Clear and populate features
            panelFeatures.innerHTML = '';
            features.forEach(feature => {
                const li = document.createElement('li');
                li.textContent = feature.trim();
                panelFeatures.appendChild(li);
            });

            panel.classList.add('active');
            backdrop.classList.add('active');
            document.body.style.overflow = 'hidden';
        }

        function closePanel() {
            panel.classList.remove('active');
            backdrop.classList.remove('active');
            document.body.style.overflow = '';
        }

        cards.forEach(card => {
            card.addEventListener('click', () => openPanel(card));
        });

        closeBtn.addEventListener('click', closePanel);
        backdrop.addEventListener('click', closePanel);

        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') closePanel();
        });
    });
</script>

